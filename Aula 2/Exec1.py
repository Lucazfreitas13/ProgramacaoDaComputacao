print("    Programa para Calcular o Perímetro e sua Área")
print("     Informe as Informações Solicitadas Abaixo:")
Altura=float(input("Informe a Altura: "))
Largura=float(input("Informe a Largura: "))
ÁreaRetângulo=(Altura*Largura)
Perímetro=(Altura+Largura)*2
print("A área do Retângulo= ",ÁreaRetângulo)
print("O Perímetro= ",Perímetro)
