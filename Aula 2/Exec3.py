#Escreva um programa em Python que solicite ao usuário a distância entre duas cidades e o tempo de viagem.
#O programa deverá calcular e exibir a velocidade média de um carro que vai de uma cidade para outra.
print("    Programa para Calcular Velocidade Média")
print("   Informe as Informações Solicitadas Abaixo:")
distancia=float(input("Digite a distância entre as cidades: "))
tempo=float(input("Digite o tempo de viagem entre as cidades: "))
velocidadeMedia=int(distancia/tempo)
print("Sua velocidade média é: ",velocidadeMedia,"Km/h")
